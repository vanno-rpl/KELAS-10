let angka = 10; // --> Tipe data: number (integer / bilangan bulat)
let kata = "Saya belajar javascript"; // --> Tipe data: string
let pecahan = 2.5; // --> Tipe data: number (float/desimal)


// Menampilkan nilai dan tipe data variabel 'angka'
console.log(angka);

// Menampilkan tipe data variabel 'pecahan'
console.log(typeof(pecahan));

// Menghasilkan tipe data 'INFINITY'
console.log(1/0); 

// Menampilkan tipe data 'NaN' (Not a Number)
console.log("JavaScript" / 2+5);




let isi = "Belajar";

// Menggabungkan dua kata
console.log(isi + kata); // --> Menampilkan 'Belajarsaya belajar javascript'
console.log(`LunarCodeZ ${angka} ${kata} ${isi}`); // --> Menampilkan 'LunarCodeZ 10 saya belajar javascript Belajar'
console.log("LunarCodeZ"+ angka + kata + " " + isi) // --> Menampilkan 'LunarCodeZ10Saya belajar javascript Belajar'



// Tipe data BOOLEAN
let kondisi = true;
